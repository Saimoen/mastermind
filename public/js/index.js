function changeCouleur1(select) {
    let choix1 = document.getElementById('choix1');
    let couleur = select.value;

    choix1.className = 'm-auto';

    if (couleur === '1') {
        choix1.style.backgroundColor = 'white';
    } else if (couleur === '2') {
        choix1.style.backgroundColor = 'black';
    } else if (couleur === '3') {
        choix1.style.backgroundColor = 'green';
    } else if (couleur === '4') {
        choix1.style.backgroundColor = 'yellow';
    } else if (couleur === '5') {
        choix1.style.backgroundColor = 'red';
    } else if (couleur === '6') {
        choix1.style.backgroundColor = 'blue';
    }
}

